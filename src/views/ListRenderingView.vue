        by using the component that is created in the components section with the name 'Componentforlist.vue' .</p>
    <Componentforlist
        v-for="(unit, index) in newunits"
        :key="index"
        :unit="unit"
      ></Componentforlist>

    </div>
    </div>
  </template>

<script >
